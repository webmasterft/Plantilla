            <p><span class="capital">L</span>La protección de la cervicabra es una de las prioridades para los guardaparques y técnicos que trabajan en el Área Nacional de Recreación El Boliche. Se trata de un venado de color rojizo con tintes café oscuro y negro, y está en peligro de extinción.
            </p> 
            
            <p>El principal depredador de este animal ha sido el hombre. Es uno de los seres más asediados por los aficionados a la caza. 
            El Boliche lleva este nombre por ser una formación natural en medio del bosque, similar a un plato hondo (boliche). Es un área de recreación que tiene un promedio de 600 visitantes por semana. En los fines de semana es muy concurrido por su cercanía con Machachi, Latacunga, o Quito. </p>
            
            <p>Guisela Salguero, una de las técnicas del Ministerio del Ambiente, explica que desde el 2012 se aplica en la zona el Sistema de Manejo de Visitantes.    La idea es distribuir adecuadamente la carga de turistas, para evitar que una presencia masiva cause daños, sobre todo en áreas frágiles. 
            Sin embargo, hay quienes no respetan la naturaleza. Al caminar por el sendero Quishuar, es común encontrar  troncos totalmente pelados de los yaguales (como se conoce en la zona al polylepis o árbol de papel). </p>
            
            <p>Los turistas arrancan su delicada corteza, formada por varias láminas que protegen al tronco frente al frío o al fuego. 
            El reto en El Boliche es equilibrar su faceta de área recreativa con los esfuerzos  por conservar su biodiversidad.</p>


            <p><span class="capital">E</span>l panóptico de Quito, el penal García Moreno, se convirtió en parte del imaginario de los capitalinos. Hay quienes lo comparan con la figura del Aleph, de Jorge Luis Borges: un punto donde se refleja una sociedad entera, para indicar que el mundo inferior es solo un reflejo, una aproximación, al superior. Este espacio, con el traslado de los internos a Cotopaxi, se va vaciando, convirtiéndose en lo que el escritor Santiago Páez denomina: “Un fantasma más de Quito”. </p>

            <p><img class="imgLeft" src="img/patrimonio/internas/1.jpg">En sus corredores, ahora estrechos, reducidos por el hacinamiento, se escribió la muerte de Eloy Alfaro, pero también las condenas de los presos políticos y de los internos comunes que, desde 1874, llegaron a la estructura construida por el expresidente de la República García Moreno. </p>
            
            <p>Se tienen noticias de que fue el Pabellón E, celda 13, el lugar que en enero de 1912 recibió al líder de la Revolución Liberal, que luego fue linchado por una turba. 
            En la calle Rocafuerte, donde se levanta la construcción, a pocos les interesa que el edificio tenga su referente en el modelo de Jeremías Bentham, el arquitecto de las ‘instituciones ideales’. Estas estructuras fueron inicialmente pensadas para fábricas y aplicadas a instituciones educativas. </p>
            
            <p>En Quito, este modelo se replica con su concepción inicial: un sistema de vigilancia donde el poder se ejerce desde el centro, para corregir las malas conductas y, a la par, guiar hacia el buen camino a la sociedad. 
            Para el capitalino, en lo alto del Centro, la figura estaba latente, amenazante, como un recordatorio del futuro que podría tener si se atrevía a  infringir las normas.
            Páez se acerca a esta efigie quiteña como a una figura de ‘diciplinamiento’. Tiene presente la voz de su abuela, repitiendo: “Si sigues así, vas a terminar en el panóptico”. En el penal hay una suerte de dualidad: un sitio de horror y de fascinación, con el que cada quiteño tiene un vínculo, cercano o distante. </p>
            
            <p>Se convirtió en el referente de la reclusión de personas, para que sirva de escarnio del resto de la sociedad. Juan Paz y Miño, cronista de Quito,  refiere la época en la que estaban vigentes las penas infamantes y era parte de la rutina colgar a los presos. Apenas en 1895 se introduce el criterio de los derechos de los  privados de la libertad. 
            Para Michel Foucault, en ‘Vigilar y castigar, nacimiento de la prisión’: “La generalidad carcelaria, al jugar en todo el espesor del cuerpo social y al mezclar sin cesar el arte de rectificar al derecho de castigar, rebaja el nivel a partir del cual se vuelve natural y aceptable el ser castigado”.</p>
            
            <p>Para los cerca de 3 000 comerciantes de San Roque, el penal es el vecino diario de la jornada de trabajo. En el caso de Vinicio Sosa, líder barrial,  hablar del penal es recordar los recorridos que, en su época de estudiante de Derecho, eran parte de la jornada académica. Otro mundo. Así describe este dirigente al predio patrimonial. 
            En el sitio hay algo claro, para ingresar, las visitas no pueden vestir ropa oscura ni portar armas, aparatos electrónicos, entre otros artículos. Frente al ingreso al penal, se distribuye una especie de percheros, a cargo de quienes por 50 centavos guardan las pertenencias de los visitantes. </p>
            
            <p>El edificio no siempre estuvo en las entrañas de Quito, se levantó en un espacio que, para la época de la construcción, constituía parte de la periferia; cuando para ir a Guápulo se emprendían peregrinaciones ­y la ciudad llegaba, hacia el norte, hasta San Blas y, con suerte, hasta La Alameda. El terreno era parte de la hacienda Algovacín, de propiedad de la familia González.
            En esos años, repasa Paz y Miño, no había una cárcel oficial, más allá de las  municipales o de los cuarteles. Casas o cuartos del Centro eran los sitios destinados para los reos. La población penitenciaria no era abundante. “El penal García Moreno abastecía e incluso sobraba espacio”.</p>
            
            <p>Esa realidad cambió. Entrar al centro de rehabilitación es distinguir celdas abarrotadas y los brazos de los internos saliendo por las rejas. Hasta enero de este año, según datos del Ministerio de Justicia, 3 255 personas ocupaban el lugar. La capacidad actual es de 1 476.  El 21 de febrero, 351 internos fueron trasladados al centro que se construye en Cotopaxi. 
            El penal será desocupado hasta junio de este año. El futuro anunciado para la construcción es convertirlo en un hotel. La obra forma parte del denominado eje de la 24 de Mayo, que incluye El Censo, Qmandá, La Ronda, el bulevar y San Roque. Pero, ¿un hotel será suficiente para redimir años de pesadumbre? ¿Debajo de la estructura se encontrarán nuevos senderos de un Quito oculto, inexplorado y, al mismo tiempo, latente? </p>
            
            <p>Con el final de esta etapa de la ciudad, para Páez queda reelaborar el imaginario del penal, construir una nueva huella... Será que el capitalino dirá, como Borges en ‘El Aleph’: “¿Lo he visto cuando vi todas las cosas y lo he olvidado? Nuestra mente es porosa 
            para el olvido”. 
             Como sucede con la línea de buses Camal-Hipódromo, que lleva a los pasajeros de un fantasma a otro -pues ambos destinos dejaron de existir- hay que preguntarse: “¿Qué tipo de fantasma vas a constituir el panóptico”.

            </p>

           

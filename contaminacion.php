            <p><span class="capital">L</span>os tapires Helena y Sam tuvieron a su sexta cría hace 10 días. Y otro miembro se sumó a una letárgica pandilla de perezosos. El astuto tapir y el tierno perezoso que no se desprende de su mamá son parte de planes de reproducción en cautiverio del Parque Histórico de Guayaquil.  En lo que va del año han nacido cuatro crías, el 2013  fueron nueve. </p>
            
            <p>Con esto se busca, en parte, aliviar el estado de vulnerabilida de ciertas especies, explica la veterinaria Ana Piña. </p>
            
            <p>Ellos a su vez forman parte de las 50 especies típicas de la Costa que habitan este zoo, que alberga a un total de 330 ejemplares distintos.  </p>
            
            <p><img class="imgLeft" src="img/biodiversidad/internas/2.jpg">A las crías las tienen hasta un año y luego los envían a centros de rescate y reservas para intercambios. Así se impide la consanguinidad, que puede causar malformaciones y alteraciones de tamaño. </p>
            
            <p>En el Libro Rojo de Mamíferos del Ecuador hay 214 especies de mamíferos en distintas categorías de vulnerabilidad. En el país hay 404 especies.   
            
            En ese listado de peligro se incluye al perezoso de dos uñas, debido al tráfico ilegal. En Colombia, donde cuentan con una estrategia nacional para su conservación, se calcula que el 50% desapareció en la última década, en el país faltan estudios. Los bebés son los más demandados, pese a   que el  80% muere al no soportar la separación de la madre. </p>
            
            <p>En el Parque hay esperanzas para el pequeño que nació hace dos semanas. Tras 12 meses de gestación vivirá abrazado a su madre por nueve meses y alcanzará la madurez a los cinco años. Está en el área de neonatos, alejado de visitantes y  lleno de mimos. </p>
            <p>En este espacio también crece el  bebé tapir, que perderá sus manchas blancas a los cinco meses. Solo sale del sitio cuando Helena, su madre, debe alimentarlo. </p>
          
            <p>El tapir, danta o gran bestia también es una especie amenazada. En el país existen tres: el amazónico que está en peligro, el andino o pinchaque, en peligro crítico; y el Chocó, de la costa o bairdii que reporta datos insuficientes. </p> 

              <p>Investigadores locales creen que este último ya está extinto. 
              Helena y Sam son amazónicos. Sus otros hijos fueron liberados en sitios bajo vigilancia, como en la reserva Manglares-Churute. 
              La Estrategia Nacional para la Conservación de Tapires reporta que aproximadamente 32 ejemplares amazónicos son mantenidos en cautiverio. En promedio se detecta una nueva cría cada 18 meses. </p> 

                <p>Según la Legislación Ambiental Secundaria (Tulas), vigente en el país, la reproducción en cautiverio se aplica con fines comerciales, ya sea para investigación, dentro y fuera del territorio. 
              También para investigación médica y farmacéutica. 
              Francisco Prieto, subsecretario de Biodiversidad, agrega que también se reproduce especies en cautiverio para conservación, por ejemplo, el cóndor andino y el papagayo de Guayaquil. Ambas especies están en peligro. Este método permite la supervivencia y posterior reintroducción en sus hábitats naturales. </p>

                  <p>Hayra (Príncipe) y Killari (Luz de Luna) son dos cóndores nacidos en el Zoo de Guayllabamba, pero para que sean liberados necesitan ser tres y así no poner en peligro su vida. En Ecuador hay 50 silvestres y 19 en cautiverio.
                  En la Fundación Jambelí, en la vía Guayaquil-Machala, se enfocan en salvar de la extinción al papagayo de Guayaquil, al mono araña y al cocodrilo de la Costa.  Este  último también cuenta con un programa de reproducción asistida en la isla Santay. </p> 

                    <p><img class="imgRight" src="img/biodiversidad/internas/1.jpg">14 zoocriaderos en seis provincias (Pichincha, Cañar, Cotopaxi, Tungurahua, Pastaza y Guayas) tienen  planes de reproducción en cautiverio.    
                    Ahí están venados de cola blanca, en peligro de extinción, mariposas, anfibios, insectos, guantas, etc.  
                    La reinserción no es posible en todos los casos, reconoce Nelson Chiriboga, director del zoológico El Pantanal.  Para los anfibios y reptiles  es más fácil adaptarse a su hábitat, porque no están ligados a la madre, como mamíferos. </p>

                      <p>En algunos casos se aplica el semicautiverio para estos últimos, es decir, viven una libertad controlada en amplias extensiones de terreno. 
                      Un programa exitoso de reproducción y reinserción es de tortugas, en el Parque Nacional Galápagos. Esta semana nació el ejemplar  100. </p>


            <p><span class="capital">F</span>uriosas olas de 10 metros, vientos helados y huracanados. Es el mar de Drake, entre Tierra del Fuego y la Antártida, una de las rutas de navegación más temidas y peligrosas del mundo y el camino obligado para llegar al continente helado.
            Luego de 43 horas en un mar indómito, que marea hasta a los marineros más experimentados, tripulantes y periodistas a bordo del Ary Rongel, el barco de apoyo oceanográfico de la Marina brasileña, llegaron a la isla Rey Jorge, en el archipiélago de las Shetlands del Sur. 
            </p>Ahí está ubicada la base de Brasil. 
            
            <p>El pasaje de Drake, con 1 000 km2 de extensión, corresponde a tres cuartos del viaje desde la ciudad chilena de Punta Arenas hasta la Antártida. 
            Los barcos en este tramo  son buenas plataformas para el avistamiento de ballenas, delfines y aves marinas, como el petrel gigante, albatros y pingüinos y focas. El paisaje es fantástico y hay aire puro. </p>
            
            <p>“La travesía del Drake es temida porque es uno de los peores mares del mundo, si no es el peor. De oeste a este, recibe muchos vientos, frentes fríos, sin barreras físicas para interrumpir las olas que en el verano pueden llegar a ocho o 10 metros. Pero buenos mares nunca forjaron buenos marineros ”, explica el capitán Ricardo Magalhães, de 31 años.
            Fue justamente su homónimo, el portugués Fernando de Magalhaes, el primero en realizar esta hazaña en 1520, completando la travesía entre el Atlántico y el Pacífico, financiado por la Corona española que buscaba una ruta comercial.</p>
            
            <p>Después de muchas noches de mal sueño, comprimidos contra las náuseas y varios objetos quebrados en el vaivén constante del navío, una bandada de petreles del Cabo da la bienvenida a los boquiabiertos pasajeros del Ary Rongel .
            El trayecto marítimo desde la Patagonia chilena hasta la Antártida revela una naturaleza bella, pero también hostil.</p>
            
            <p>Al inicio, las aguas son tranquilas. El navío pasa por el Estrecho de Magallanes y avanza por los canales chilenos, un trayecto de unos 600 km que demora 30 horas en ser recorrido.
            Este corredor es conocido también como Avenida de los Glaciares, debido a las enormes masas de hielo que, a pesar del derretimiento, aún se acumulan en las montañas en el final de este verano austral.</p>
            
            <p>De allí sigue por el pasaje Richmond hasta llegar al mar de Drake, que marca el encuentro entre el Atlántico y Pacífico.
            Según estudios químicos de dientes de peces hallados en rocas sedimentarias oceánicas, el paso de Drake estaba cerrado hasta hace más de 40 millones de años. Antes de que  este pasaje se abriera, los océanos Atlántico y el Pacífico estaban completamente separados y la Antártida carecía de capa de hielo. Al unirse, la corriente circumpolar antártica comenzó a fluir, enfriando significativamente la Antártida.
            </p>



            <p><span class="capital">E</span>n la primera página de este Diario, del 5 de julio de 1930, se lee un anuncio cubierto por la pátina de los años: el Hotel Viena, de Quito, ofrecía en su menú  ‘seviche de corvina’. 
Julio Pazos, poeta y estudioso de nuestra gastronomía, recoge el dato del aviso en su libro ‘El sabor de la memoria’ (historia de la cocina quiteña,  Fonsal, 2008), un texto que despierta añejos sabores, atados a nuestra identidad. 

            </p> 

            <p>Este dato contradice la creencia de que el cebiche de pescado, con limón, aceite, y otros ingredientes, cautivó a los capitalinos entre los setenta y ochenta.  Pero allí se expandió.</p>
            <p>¿Por qué los quiteños, en una ciudad montañosa (2 800 m.s.n.m.), aman tanto al cebiche costeño? Según el INEC,   en el Censo del 2010 de 170 150 locales de comida, 6 317 son cebicherías y restaurantes que también venden cebiches. 
            Pazos, en su primera explicación, apuntala el gusto de los quiteños  con una razón más sibarita y de gozo: por historia el capitalino ha sido goloso -siempre degustó las mistelas, los suspiros, los tamales y las humitas- y vio en los cebiches una golosina deliciosa y exótica que incorporó  a su menú .            </p> 

            <p>
              Rastreando su origen hallamos la segunda vía de entrada a  Quito, según Pazos: el tren, en 1908, traía pescado de Guayaquil en inmensos bloques  rectangulares de hielo, envueltos en paja y cubiertos con sal, para que el pescado llegara fresco. 

            </p>

            <p><img class="imgLeft" src="img/territorio/internas/1.jpg">Pazos cree que en el Quito de los treinta se comía pescado frito. Y el cebiche estaba destinado a las élites que tenían dinero, para asistir a sitios como el Hotel Viena. </p>
              
              <p>En otro aviso de este Diario, de 1938, se lee: Pescado de Mar. El local:  una bodega de La Alameda de The Tesalia Spring Company. Fernando Jurado Noboa, autor de una treintena de libros sobre identidad quiteña y genealogía, rememora que en la segunda administración del Dr. Velasco Ibarra, mediados de los cuarenta, su gabinete, integrado por prominentes personajes guayaquileños (Adolfo Jurado, Nicolás Valdano Raffo, Carlos Guevara Moreno, quiteño residente en el Puerto), trajo el cebiche a Quito. “Las mujeres -dice- formaron clubes e invitaban a  varias familias a saborear cebiches de concha, camarón y pescado, como  platos exquisitos”. 

            </p>

            <p>
              Según Jurado Noboa, otro personaje clave fue el comerciante Antonio Pino Icaza, quien instaló un negocio de  venta de autos y repuestos, a principios de los treinta, en Quito.  “Los entusiastas hermanos Molestina  González, que trabajaban para Pino, difundieron el cebiche de concha y camarón, insumos que traían en aviones de la empresa Area, invitando a sus amigos; se sumaron las hermanas Lucía y Pepita”. Jurado explica que  el cebiche se vendió a la clase media en los restaurantes más cotizados, como El Viena. 
              O El Metropolitano (1925, en las calles Chile y Venezuela). 

            </p>

            <p>
              
            “Los ingredientes eran los mismos que usaban en Guayaquil: aceite de oliva, limón y sal, para la concha prieta, y trozos de tomate y cebolla paiteña, para el de camarón”.
            Por testimonios de la época -subraya Jurado- los quiteños, en un 90 por ciento, no conocían el mar. “Sentían pavor al océano, a las olas ”. Gracias a la apertura de la vía Quito- Santo Domingo (1963), la clase media quiteña empezó a viajar a Esmeraldas . Allí halló   el cebiche y lo hizo suyo.   
            Pazos reconoce que en Riobamba, en los sesenta, a pesar de ser una estación principal del tren, no había cebicherías, más bien restaurantes típicos  en torno al ferrocarril que iba a Durán. Lo recuerda porque estudió, un año, en el Colegio Salesiano Santo Tomás Apóstol.

            </p>

            <p>“Más bien en Ambato había cebiches de pescado y de concha en los mercados, en los años sesenta”.  
              Sin embargo, Pazos, basado en documentos antiguos, relata que en 1767 (año de la expulsión de los jesuitas), los  ecuatorianos y panameños comían ostiones, un molusco que se incrusta en las rocas marinas. 

            </p>

            <p>
              “No solo disfrutaban las élites, servido con limón y sal era muy popular;  lo comía la gente de a pie”.  
              Pazos refiere que el uso del  tostado con el cebiche, un grano grande, hoy aderezado con manteca de chancho, tiene sus orígenes prehispánicos. 
              En el trópico sería inusual acompañar el cebiche con el tostado serrano. El ‘chifle’ o plátano asado es más costeño. 


            </p>

            <p><span class="txtColor">La sazón manabita </span><br>
            Jimmy  Loor es un manaba hecho y derecho. Tiene 47 años y ha trabajado 16  en Rincón Manabita, una de las cebicherías más populares de  la ciudadela Jipijapa, zona de  mariscos.
            Dice que el Rincón fue fundado hace 32 años, en un garaje de la Gaspar de Villarroel, por doña Josefa Andrade, madre de un cuñado de Loor, y nacida en Canoa. Cada  sábado, en el local  de la Tomás de Berlanga e Isla Santa Fe, recibe a 500 clientes (de 08:00 a 16:00); los domingos hasta 700. Cada semana, en un camión refrigerado, trae 400 libras de camarón de Manta y Pedernales, y 6000 conchas prietas de Esmeraldas. </p>
            
            <p>Preparan cazuelas de mariscos, viches, sopas marineras.  El costo de los cebiches: un promedio de USD 7,45.
            Loor reconoce que no sería comida manabita sino incluyeran maní de su provincia, cebolla perla en los cebiches y aceite de oliva. ¡Ah!, el ají  ‘ratón’, poderoso.  </p>

            <p>Loor da un hecho interesante para conocer el auge de los cebiches manabitas en Quito  -como el Jocay y Rumiñahui-, entre otros: a mediados de los setenta su provincia sufrió una sequía y cientos de manabitas   emigraron a Quito y a otras ciudades trayendo su rica sazón. </p>
            <p>Lo mismo se repitió en 1983, cuando el fenómeno de El Niño devastó la provincia.</p>

            <p>Al mediodía del pasado miércoles, Rincón Manabita, que luce los colores amarillo y verde de la provincia, está lleno.  
              Decenas de golosos quiteños disfrutan los cebiches, en especial los de concha y pescado. O el viche  de dorado o picudo que lo preparan  con una pasta de maní. </p>

              <p>Loor recuerda que en los setenta funcionaban otras cebicherías: El Padrino y  Portugal, las cuales tenían mucha acogida entre los capitalinos.
</p>


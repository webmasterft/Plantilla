      <p><span class="capital">E</span>l código de silencio,impuesto por el crimen organizado, es la antítesis del polvorín que explota con cada manifestación de la narcocultura. Telenovelas, música, libros son parte de un fenómeno instalado en el imaginario popular colectivo, con una mezcla de morbo y fascinación por la vida de los cabecillas de las bandas.</p>
      
      <p>En Badiraguato, donde nació Joaquín ‘El Chapo’ Guzmán, una trabajadora municipal respondía a la cadena Univisión: “Se escucha de él, pero no se puede decir nada para bien o para mal. No lo conocemos ni nada”. El ‘Non vedo, non sento, non parlo’ se instaló tras la captura del líder del cartel de Sinaloa... o Del Pacífico, para “no afectar a la imagen turís­-
      tica de ese estado mexicano”. </p>
      
      <p>Pero a ‘El Chapo’ solo le falta la telenovela para que salte definitivamente del ámbito delictivo al universo simbólico de Latinoamérica. Ya está en libros de no ficción y le compusieron su narcocorrido. Se ofrecen ‘tours’ hasta su casa, y en Culiacán, Macorito y ­
      Guamúchil hubo marchas pidiendo su liberación, bajo la leyenda ‘I love Chapo’.
      Esa suma de manifestaciones  deshumaniza al narco y lo eleva a figura de culto, una deidad 
      sea benigna o maligna. Además, naturaliza el fenómeno y valida una cultura que, como tal, maneja su ética y su estética. La ética del rápido triunfo y el exceso. La estética de lo ostentoso y melodramático.</p>

      <p>Billetes, drogas, armas, camionetas blindadas y mujeres se reúnen bajo un tono kitsch; que, a su vez, agrupa lo narco, lo popular, lo gringo, lo colombiano, lo mexicano, lo religioso. Todo en una sopa de barroquismo Caribe, que alimenta al personaje ‘hijo de la injusticia’ y transforma su vida en una ­‘hazaña de valientes’.
      El atractivo ha desatado un síndrome según el cual los jóvenes se comportan como miembros de los carteles sin formar parte de los mismos. La fascinación se muestra desde el simulacro, con un lenguaje que asume expresiones como ‘verraco’, ‘coronar una vuelta’, ‘embolatar’, y una ropa de marca o de copia pirata que arma una pinta extravagante. </p>
      
      <p>El carisma de esos personajes implica tal atractivo que sobrepasa los daños en el entramado social, desde un juego paradójico entre realidad y ficción. Mientras México y Colombia gastan millones en la lucha contra el narcotráfico, nuevos escenarios popularizan el fenómeno y legitiman desde el entretenimiento aquello que es denunciado en las secciones judiciales de los medios.
      Hernán Casciari, en su columna ‘espoiler’ de Eldiario.es, aplaudía la madurez del colombiano para ver  su tragedia social en alta definición y narrada de forma cruda. Se refería a ‘El patrón  del mal’, telenovela que le ha valido laureles a Andrés Parra por su interpretación de Pablo Escobar. Tras apuntar que el problema con esta historia increíble es que no es increíble, Casciari advierte: “Durante esta serie tu cerebro pisará en firme la realidad, pero tu corazón disfrutará los abismos muy ambiguos de la ficción. Y eso genera conflictos internos”. Pero luego confiesa con vergüenza, “ahora que terminé la maratón, lo único que quiero en la vida es una camiseta con la cara de Pablo”.
      Ahora ‘El patrón del mal’ se retransmite en la TV ecuatoriana, sus episodios se suman a la lista de audiovisuales de temática narco que bombardean la programación. Ecuador, como buen vecino, acogió las 17 ficciones que se produjeron, del 2003 al 2013, en Colombia.</p> 
      
      <p>Respecto de estas realizaciones neorrealistas, el ensayista colombiano Omar Rincón dijo a Pagina12 que “las  narconovelas están siempre en primer lugar en sintonía, pero siempre que no sean críticas frente al fenómeno sino que sean ­
      historias indulgentes con los narcos (...) Así, el narcotraficante es un héroe popular”. Los títulos de la telenovelas se corresponden con la pleitesía hacia sus personajes: ‘el patrón’, ‘el señor’, ‘la reina’... 
      
      <p>Con una narración focalizada en el lujo, la lujuria y la perversidad, los productos y los espectadores trazan una relación de contradicciones. Temor y admiración. Culto y denuncia. Placer y culpa. En sociedades de éxitos escasos y amenazadas por la pobreza, el narco es visto como un héroe rodeado de pompa e impunidad. Su modo de vida antisistema y criminal busca justificarse desde la supervivencia y el revanchismo social.</p>
      
      <p>También está el carácter mágico-religioso que envuelve a los narcos. Muchos de ellos han recibido la adoración de sus comunidades, pues las han retribuido con obra social. Construcciones y donaciones, que halagan en situaciones de pobreza, convierten a estos personajes en redentores y santos, en relación con algunas tradiciones sincréticas del catolicismo y el culto a la muerte. 
      
      <p>Pero esas dádivas tienen tanto de generosidad como de control, un control impuesto desde el falso respeto producido por el miedo. Como evidencia de ese culto, de plegaria y agradecimiento, persiste en la retina la imagen del paisa enjuto, de bigote ralo y sollozante que ante la muerte de Escobar declaraba a la televisión colombiana: “Se repite aquí en Colombia la historia de Cristo”. 
      
      <p>En México son frecuentes las peregrinaciones hasta la capilla de Jesús Malverde, bandolero sinaloense, el ‘Ángel de los pobres’ o ‘Santo de los narcos’. Rezos y escapularios cuelgan de esa ermita, parada obligada de los ‘narcotours’ que ofrecen taxistas de la región y que llevan a casas, discotecas y aceras otrora encharcadas de sangre. Los choferes dicen que los turistas exigen ir a esos sitios ­antes que a las playas.  
      Así, con tanta fascinación alrededor del narco, la mecánica del ‘nadie supo, nadie vio’ trocó en charla de sobremesa y truculento esparcimiento.</p>


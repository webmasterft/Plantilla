            <p><span class="capital">C</span>uenta Jonathan Sperber en su magnífica y reciente biografía de Carl Marx, que el ideólogo del comunismo moderno se frustraba mucho durante su exilio en París, porque su rudimentario francés no le permitía llegar con sus escritos a los socialistas franceses. A Marx poco le servía dominar el alemán, su lengua materna, pues en aquellos años (1840), mientras todo alemán culto hablaba y leía francés, en Francia casi nadie entendía alemán.</p>
            
            <p><img class="imgLeft" src="img/sociedad/internas/1.jpg">Este episodio de la vida de Marx sirve para ilustrar cómo el ser humano ha buscado un idioma universal o una “lingua franca” para comunicarse con sus semejantes.  
            Esta necesidad de hallar el idioma común hizo que el polaco Lázaro Zamenhof presentara en 1887 el esperanto, un idioma inventado por él y cuyo fin era convertirlo en el idioma que la humanidad entera compartiría. La idea nunca cuajó.</p>
            
            <p>Lo que ocurre en los tiempos modernos con el inglés y en el pasado con otros idiomas parece ser muy distinto a lo que ocurrió con el esperanto. Su uso globalizado no obedece a un acuerdo entre gobiernos ni porque sea el idioma más hablado del orbe. Se han esbozado muchas explicaciones para esta globalización del inglés. Para Robert Phillipson, el danés que lanzó la tesis ‘Imperialismo lingüístico’, el pre­dominio del inglés es producto de una imposición imperial 
            de los anglosajones a sus colonias. Sin embargo, otras corrientes académicas que han estudiado el fenómeno desde un mayor número de ángulos sostienen que el inglés ha terminado convirtiéndose en la “lingua franca”  de la actualidad, como consecuencia de un complejo proceso de globalización y de crecimiento del capitalismo moderno.</p>
            
            <p>Sin duda una de las tesis más atractivas es la que sostiene que los idiomas, en general, son una suerte de ‘commodity’ o mercancía y que el inglés, en particular, es el que ofrece mayores ventajas.  
            Según la lingüista escocesa Deborah Cameron, los motivos para adquirir o mantener determinado idioma, a los que considera como auténticos símbolos de identidad o vehícu­los de prestigio de alta cultura, están cada vez más vinculados a cálculos económicos. Mientras más y mejor puede un idioma transformarse en un bien económico, mayor posibilidad de éxito tiene. </p>
            
            <p>El economista suizo Franç­ois Grin midió, el 2001, el valor que el inglés podía tener entre trabajadores en Suiza. Luego de comparar los ingresos con el dominio del inglés encontró que había una relación directa entre los sueldos más altos y el mayor conocimiento del inglés. Cameron sostiene que también abundan los estudios que hablan sobre las ventajas invisibles que tienen aquellos países cuyo primer idioma es el inglés, al no tener que invertir recursos en educación de lengua extranjera o traducción.
            Por estas ventajas ciertos países han pensado incluso en declarar al inglés como idioma oficial a más 
            del suyo propio. Los casos de ­Japón y Rwanda son dos, aunque en ninguno de los dos 
            haya prosperado la iniciativa. Sin embargo, dice Cameron, esto evidencia la importancia que el inglés puede tener para la  economía.</p>
            
            <p>Otro ejemplo de la ‘comodificación’ del inglés es lo que ocurre en la India.  Ahí, uno de los objetivos de las políticas de educación es aumentar la proporción de la población que hable bien inglés. Eso ha forzado a las familias más adineradas a  enviar a sus hijos a colegios ingleses, donde pueden adquirir mayor dominio del idioma y diferenciarse de la gran masa. El inglés Kingsley Bolton, otro estudioso del tema, también sostiene que en China el inglés se está convirtiendo en un prerrequisito para avanzar en el nuevo orden económico, aunque las calificaciones en los cursos muchas veces son usadas por autoridades como mecanismos para impedir que determinadas personas alcancen ciertos empleos.</p>
            
            <p>Ingrid Piller, profesora de lingüística aplicada de la Universidad de Sídney, Australia,  ha puesto atención a la función del inglés como un ‘commodity’ en ciertos sectores menos presentables de la economía global. Por ejemplo en temas que tienen relación con el tráfico de mano de obra barata o sexual. La enseñanza del inglés, por ejemplo, es promocionada por ciertas agencias para que mujeres rusas consigan esposos en el exterior. El inglés también se ha consolidado como un ‘commodity’ en el mercado del sexo y la pornografía.</p>
            
            <p>Deborah Cameron también reflexiona sobre el papel del inglés en el capitalismo moderno. Sostiene que como resultado del traslado de ciertas industrias desde las economías más avanzadas a países del tercer mundo se está generando una necesidad especial para que las comunicaciones entre ambos sectores se haga en inglés.  El trabajo de “data processing” y la atención al cliente están siendo cada vez más asumidos por personas que están fuera de los países ricos y que se hallan a distancias muy considerables. En Inglaterra, por ejemplo, todo el servicio de información sobre los trenes lo hacen en un “call center” en la India. Estos servicios “offshore” son un incentivo más para que gente en el tercer mundo aprenda inglés. </p>
            
            <p>Pero dónde queda la tesis del danés Philippson sobre el ‘Imperialismo lingüístico’ en este escenario. Cameron sostiene que quizá el vínculo clave entre colonización y globalización es que la colonización inglesa resultó en un económicamente poderoso bloque de habla inglesa. Desde un punto de vista capitalista, finalmente, el mundo anglosajón fue más exitoso. Un bloque con el que muchos grupos alrededor del mundo han querido y quieren hacer negocios.
            </p>



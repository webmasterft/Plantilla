            <p><span class="capital">E</span>l problema del nihilismo, propio de la modernidad, abre un catálogo más o menos inagotable donde se pueden advertir diferentes doctrinas de pensamiento atravesadas en todo o en parte por el pensamiento nihilista, que nos remitirá —más temprano que tarde— a la idea de la decadencia de los valores (sea para su deconstrucción, sea para su construcción). 
             </p>
              <p>Hablar de nihilismo es comenzar con una cuestión cuyo entendimiento no arriba de manera pacífica a una solución. De todos modos, como dice Franco Volpi, los verdaderos problemas filosóficos “no tienen solución sino historia”. Es por esto que el libro de Volpi podría también entenderse -quizá también él lo haya concebido así- como “la historia general del nihilismo”.</p>

              <p>Al menos si nos referimos a su etimología, el nihilismo nos asocia a la idea de “la nada”; de lo que se podría deducir, seguramente sin incurrir demasiado en el error, que el pensamiento nihilista se encuentra frecuentemente con el vacío, el pesimismo, la angustia o la desesperación. Sus concepciones, tan variadas como diversas, atraviesan con dureza la historia de varias revoluciones europeas, así como movimientos artísticos “decadentes”, también ocupando un lugar en la literatura (sobre todo en Dostoievsky), pasando por doctrinas políticas como el anarquismo, o por diferentes experiencias filosóficas que van desde el nietzscheanismo (como su más grande exponente), y llegan al existencialismo y a muchas otras ideas desde la secularización de todo pensamiento hasta “el fin de la historia” como concepto.</p>
              
              <p>Nihilismo significa, desde la visión de Nietzsche, la desvalorización de los valores supremos de la sociedad. O sea el momento en que los valores tradicionales pierden su fuerza y su sentido y, con ellos, también el sentido del mundo. Dice Nietzsche: “El hombre moderno cree de manera experimental ya en este valor, ya en aquél, para después dejarlo caer; el círculo de los valores superados y abandonados es cada vez más amplio”. </p>
              
              <p><img class="imgLeft" src="img/libros/internas/1.jpg">El mundo ideal propio del platonismo es decisivamente descartado, y el hombre se enfrenta a la idea de un mundo sin dioses. Nietzsche lo dijo, tomándolo de Mainländer: “Dios ha muerto”, y con esta sentencia supuso dejar un mundo abandonado de sus concepciones morales. Así pues “el nihilismo es el que del mundo tal cual es juzga que no debería ser, y del mundo tal cual debería ser juzga que no existe”. Con el nihilismo nietzscheano caen la idea de verdad, unidad y fin. El hombre está abandonado y condenado a la voluntad de poder y al “eterno retorno de lo mismo”. </p>

              <p>Grande sería la influencia de Nietzsche, y muy dura y a veces peligrosa su asimilación. 
              Decía Thomas Mann: “Quien toma en serio a Nietzsche, quien lo toma al pie de la letra y le cree, está perdido”. Se impone a la vez aceptar el nihilismo, como lo haría Heidegger, como categoría metafísica descriptiva de la sociedad desposeída de valores, pero tratar de superarlo. Jünger, por eso, aunque coincidiendo sobre el diagnóstico del nihilismo como problema, se presenta optimista y encuentra muestras de una superación en cuanto se pueda pensar en un hombre como “nada más que un breve respiro de la naturaleza”. </p>
              
              <p>En otro sentido, con el nihilismo se piensa en los valores ya no como categorías absolutas sino relativas, aunque cuidadosamente, porque en esto podemos pensar con Camus en que existen algunos “valores sin los que no vale la pena vivir en el mundo, incluso transformado, sin los que un hombre, incluso nuevo, no merecerá que se le respete”. Diría Camus: “Si bien nosotros consentimos en prescindir de Dios y de la esperanza, no prescindimos tan cómodamente del hombre”. Para entender esta faceta de Camus basta leer ‘La Peste’.</p>
              
              <p>La falta de valores o el desuso de los tradicionales es una constante en el pensamiento contemporáneo. ¿A qué valores se refieren? Es una cuestión más complicada. Quizá también lo sea aquella de definir si ‘la desvalorización de los valores’ es siempre algo negativo o si es algo positivo cuando quienes critican lo viejo lo hacen para saber crear lo nuevo (invirtiendo en parte la frase de Gutzkow). Peligroso es, en cambio, criticar lo viejo para solamente repetirlo, incluso en una versión más descarnada. Quizá nuestra política sea un terrible y lato ejemplo de ese retorno de lo mismo y de un nihilismo en sentido negativo. 
              
              <p>Entonces, ¿qué nos ha dejado el nihilismo? Dice Volpi: “Nos ha enseñado que no tenemos más una perspectiva privilegiada -ni la religión ni el mito, ni el arte ni la metafísica, ni la política ni la moral, y ni siquiera la ciencia- capaz de hablar por todos los otros”. Así tratada la cuestión quizá podría acercarnos a la atractiva tesis de que la ‘desvalorización de los valores’ impone la misión de defender los propios, pero no para imponerlos. Nos ayudaría a reafirmar, en fin, el principio de libertad y el principio de tolerancia. Y así decir que nuestros tiempos deben reclamar por lo menos una mínima moral: la de la convivencia de la propia libertad y de los propios valores con los de los demás.

              </p>
              

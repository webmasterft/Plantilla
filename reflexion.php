      <p><span class="capital">E</span>No es su oficina ni el lugar donde trabaja, es el lugar donde investiga y escribe. Para Jorge Velasco Mackenzie escribir no es trabajar, es una forma –la única posible– de vivir. No es su oficina, es más que eso, es como si fuera su casa. Es el sitio “donde me hacen las entrevistas”, dice cuando concertamos la cita telefónicamente, para sentarnos a hablar del encierro, esa circunstancia que él ha vivido en contra de su voluntad, pero a favor de sí mismo. Para no dejarse vencer por “la enfermedad”, que es como llama al alcoholismo que lo aqueja.</p>
      <p>Y se antoja paradójico estar hablando del encierro junto a unos ventanales, de piso a techo, que permiten una vista envidiable del Parque Centenario (Guayaquil). A ratos parece que sobrevoláramos las copas de los árboles, mientras la luz de una tarde de marzo, caliente y húmeda, entra a la Biblioteca de la Casa de la Cultura Núcleo del Guayas, y Velasco Mackenzie recuerda con 
      nostalgia las tres bibliotecas que ha perdido en las separaciones con sus parejas. </p>
      
      <p>Pero también hay espacio para la ilusión: “Ahora que estoy solo, intento armar una nueva biblioteca. El otro día la veía y me daba gusto porque está creciendo, ya tengo unos 800 libros”. Y lugar para las bromas: uno de sus ‘colegas’ de la Biblioteca se acerca con un cepillo viejo para insinuarle que se peine para las fotos e inmediatamente después finge que le toma una con un Nokia tan antiguo como destartalado. 
      Solo después de que la foto ficticia ha sido tomada por el ‘colega’ empezamos a conversar (al ritmo que impone la mente dispersa de Velasco Mackenzie, que parece no querer pensar en ciertas cosas).</p>
      
      <p><span class="txtColor">¿Cómo se hace para escapar de las cárceles personales en las que todos vivimos?</span>
      No se puede; escapar de la cárcel personal es imposible. Sobre todo en el caso mío, que soy un escritor; mi cárcel es mi escritura, inventar cosas.</p>
      
      <p><span class="txtColor">Como decía Capote: “Cuando dios te da un don también te da un látigo”.</span>
      Tenía muchísima razón. Pero Capote se olvidó de mencionar a una cárcel mucho más fuerte.
      
      <p><span class="txtColor">¿Cuál?</span>
      La cárcel del lenguaje. Nosotros los escritores vivimos encerrados en una cárcel de palabras también. Dependemos del lenguaje.</p>
      
      <p><span class="txtColor">Pero no parecería una cárcel tan indeseable esta de las palabras, ¿o lo es?</span>Depende de cómo se lo tome. Por ejemplo, yo admiro mucho a Rimbaud, y él hablaba de la cárcel del lenguaje y terminó abandonando la escritura siendo todavía muy joven.</p>
      
      <p><span class="txtColor">Aprovechemos sus dotes narrativas para que describa el encierro físico, ¿qué es?</span>
      Es un espacio en el que uno no solo pierde la libertad sino la voluntad de vivir. 
      </p>

      <p><span class="txtColor">¿Qué diferencia al encierro físico del encierro emocional? ¿O son lo mismo?</span>
      Los dos son aliados.</p>
      
      <p><span class="txtColor">¿Es inevitable entender al encierro, al aislamiento, como un castigo?</span>
      Yo no lo tomé nunca como un castigo, pero creo que en los reclusorios penitenciarios el encierro sí es un castigo.
      
      <p><span class="txtColor">Entonces, ¿sinónimo de qué es el encierro?</span>
      De la pérdida de libertad, pero en mi caso asociado a la cura de mi enfermedad.</p>
      
      <p><span class="txtColor">¿Qué opina de las personas que buscan voluntariamente el encierro y el aislamiento, como los monjes?</span>
      Ellos llegan a ese punto en busca de la espiritualidad y eso es muy diferente. Pero aunque sea voluntario también tiene su nivel de sacrificio, porque ellos se están sometiendo para encontrar una especie de rehabilitación por la vía espiritual.
      </p>

      <p><span class="txtColor">¿No cree que de alguna manera todos sufrimos algún tipo de encierro; estamos atrapados en alguna emoción, en una circunstancia, en un recuerdo?</span>
      El encierro es algo que nos pasa a todos. Puede ser un encierro de orden familiar. Un encierro por carecer de afecto o por dedicarse exclusivamente a los hijos y vivir poco.</p>
      

      <p><span class="txtColor">¿Y qué cosas cree que podemos hacer para no ser víctimas del encierro?</span>
      Abordar todo desde afuera. Ver las cosas desde afuera.</p>
      
      <p><span class="txtColor">¿Como si fuera la vida de otra persona?</span>
      Y es que es la vida de otro. Ya ve, lo escritores novelamos la vida de otros.</p>
      
      <p><span class="txtColor">¿La fórmula también sirve para los no escritores?</span>
      No lo tengo muy claro. Porque pretender que no nos afecte lo que nos pasa es imposible. De todas formas, al nacer y al estar en la vida nos convertimos en testigos y personajes de ese entorno en el que vivimos. Es el ananké griego, el destino, estamos condenados a eso. </p>
      
      <p><span class="txtColor">¿Qué se llega a comprender desde el encierro?</span>
      La necesidad de estar limpio, la obligación de estar limpio.</p>
      
      <p><span class="txtColor">¿Lo peor del encierro, a más de perder la libertad?</span>
      Lo más duro que yo viví las veces que estuve en esa clínica fue no poder comunicarme con las personas que quiero.
      <span class="txtColor">De una manera simbólica, le quitaron la palabra.</span>
      Exacto. Pero yo comencé a escribir. Aunque nunca pensé escribir una novela sobre ese tema, me di cuenta de que algo tenía que quedar de eso.</p>
      
      <p><span class="txtColor">¿Se puede rescatar algo bueno de esa vivencia?</span>
      En mi caso, la novela; hay una posibilidad de crear ahí.</p>
      
      <p><span class="txtColor">¿A quién usted sí le desearía el encierro?</span>
      Al dueño de la clínica.</p>
      
      <p><span class="txtColor">¿Y a quién nunca se lo desearía?</span>
      A ninguno de mis hijos.</p>
      
      <p><span class="txtColor">¿A usted para qué le ha servido el encierro?</span>
      Dicen que para alejarme de mi enfermedad, para curarme un poco.</p>
      
      <p><span class="txtColor">Eso dicen, ¿usted qué cree?</span>
      No mucho.</p>
      
      <p><span class="txtColor">¿Qué se necesita para salir vivo del encierro?</span>
      Algo que no tenemos los que padecemos una adicción y todo el mundo te pide que lo tengas. Mis amigos me dicen: todo es cuestión de fuerza de voluntad. Pero eso es lo que no tiene un adicto.
      <span class="txtColor">Pero usted logró salir del encierro.</span>
      Estoy saliendo, querida, estoy saliendo... No he podido salir completamente, como yo quisiera.</p>


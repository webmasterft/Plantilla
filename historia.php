            <p><span class="capital">C</span>uando niño, en mi ciudad natal Haifa, Israel, atravesaba todos los días un callejón sin salida que, aparentemente, nadie más cruzaba.  Había algo que me atraía en este callejón y era el nombre con el que le habían bautizado.  Fue en honor a uno de los sabios judíos más destacados de la época de La Edad de Oro judía en España. La salida la encontré cuando pregunté a mi papá acerca del vínculo entre el nombre del callejón y el apellido de mi abuela. Fue ahí cuando mi papá nos contó que somos descendientes de los judíos expulsados de España. </p>
            
            <p>“¿Adónde llegará por buscar sefarditas?”, me preguntó alguna vez años más tarde un periodista español y yo, sin pensar, le respondí: “Si es necesario, llegaré hasta la Luna, y seguramente que también allá voy a encontrar sefarditas”.
            Ahora no tengo que llegar a la Luna para buscar descendientes de los sefarditas y entrevistarles para mi documental ‘América Ladina’. Puedo encontrarles casi en cada esquina en la región, y también en Ecuador, donde dejaron huellas de apellidos, costumbres y también objetos religiosos escondidos. Según investigaciones científicas basadas también en archivos de España, por lo menos el 30% de los primeros emigrantes a las Américas eran Nuevos Cristianos, es decir judíos convertidos por la fuerza al catolicismo en la época de la Inquisición y que buscaron huir de las persecuciones.</p>
            
            <p>La búsqueda de las raíces es  una tendencia mundial, que empieza con el libro ‘Raíces’ del escritor afroamericano Alex Haley. Tanto así que en nuestros días se puede decir que ser sefardita es una moda. Conocer las raíces enriquece, pero este descubrimiento no debe convertir al sefardita automáticamente en  un judío después de siglos de ausencia.
            Cada día aparecen más descendientes verdaderos e imaginarios y, junto a ellos, religiosos e historiadores que no estudiaron a fondo la historia judía. Estos supuestos estudiosos adaptan esta moda sin saber el término correcto del fenómeno. </p>
            
            <p>Dicen que hace menos de 500 años los judíos llegaron a la tierra de Atahualpa, cuando la verdad es que los que llegaron eran los “marranos” o “cripto judíos”, como les llamaron en España y Portugal a quienes intentaron ocultar su origen judío. Es más, estos inmigrantes ya no eran judíos sino hijos, nietos o bisnietos bautizados en el catolicismo. 
            Por no utilizar términos ofensivos del pasado, los descendientes adoptaron dos nombres: “sefarditas” y “conversos”. Ambos son equivocados. Según el judaísmo, los conversos (Mumarim, Meshumadim) son judíos que se convirtieron al catolicismo por voluntad propia y no a la fuerza, como en el otro caso. Los sefarditas son los judíos expulsados de la Península Ibérica que, como mi familia, mantuvieron siempre su judeidad a lo largo de estos siglos y jamás renunciaron a su fe judía. </p>
            
            <p><img class="imgLeft" src="img/historia/internas/1.jpg">De hecho, el único término correcto es “anusim” o  “forzados”. Sin embargo este concepto es desconocido entre la mayoría de los ecuatorianos que buscan sus raíces y los historiadores locales. 
            Existen otros errores de fondo en el discurso sobre el tema. Dicen que los “marranos” que pisaron tierra ecuatoriana llegaron solo a las provincias del sur, Loja y El Oro, cuando la verdad es que se dispersaron también a otras zonas del país.
            Aunque este capítulo es parte de la historia de la región, hay muchos que niegan su posible origen sefardí. Este rechazo generó asimismo un fenómeno opuesto. Muchos que son exevangélicos y mesiánicos no tienen pruebas de descendencia sefardí, pero quieren migrar a Israel. </p>

            <p>En los siglos anteriores conversos buscaron escapar del judaísmo, y ahora hay bastantes grupos, incluso en Ecuador, que visten como judíos ortodoxos, sin ser judíos según los preceptos, y exigen a los rabinos y al Estado de Israel que les acepten como judíos según la ley del retorno.   De hecho, parte de ellos no tiene ninguna prueba de su pasado sefardí, y a pesar de que sus familias perdieron la fe judía por cientos de años y quedaron solamente con algunas costumbres, exigen a los rabinos que les conviertan en un trámite 
            muy corto y sin una enseñanza judía  adecuada.</p>
            
            <p>Del otro lado, casi todos los rabinos autorizados para realizar conversiones legales, recha­zan de manera contundente a la mayoría de estos grupos, y no los dejan ingresar en  Israel ni en las comunidades judías originarias de la región. Les tratan como si fueran enemigos de la fe judía, y no lo son.
            En Israel ha habido un debate público alrededor de la pregunta quién es judío. Esta polémica no va acabar pronto, porque los ‘Hijos de Anusim’, como les llaman en Israel a los descendientes de los conversos, no tienen un diálogo verdadero con la mayoría de los ­rabinos, y el parlamento de Israel, de su lado, no intenta resolver la polémica.</p>
            
            <p>En esta disputa ha entrado el Gobierno español, que ha presentado un proyecto para dar nacionalidad española a todos los descendientes de los judíos expulsados de España y que puedan comprobar su origen. Según los españoles, hay en el mundo alrededor de 3,5 millones de expulsados. 
            La tarea de los españoles será distinguir ahora entre los verdaderos descendientes de judíos y los disfrazados. Así, los ‘anusim’ o ‘forzados’ de la región podrán cumplir su sueño de retornar, pero no a Israel como querían al principio, sino a España.
            </p>

            
